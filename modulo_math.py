#Requerimos hacer el cálculo de la raíz cuadrada de una variable llamada “y que tiene valor 81, 
# utilizando el módulo de la librería math incorporada en Python. 
# El resultado debemos asignarlo a una variable que será impresa en pantalla.

#Se usa el modulo math e importa sqrt para obtener la raíz cuadrada
from math import sqrt

#Se crea variable con número a obtener raíz cuadrada
numero = 81
#Se realiza la función cálculo de raíz cuadrada
resultado = sqrt(numero)
#Se usa la función print para mostrar el resultado
print(resultado)

